package com.javaproject;

public class Swap {

	public static void main(String[] args) {
		int a=20,b=30;
		System.out.println("before swapping");
		System.out.println("a is "+a);
		System.out.println("b is "+b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("after swapping");
		System.out.println("a is "+a);
		System.out.println("b is "+b);
		
		

	}

}
